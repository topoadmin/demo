		$district.on("districtTap", "li", function(e) {
			var $tapLi = $(this);
			$district.data("activeIndex", $tapLi.index());
			$tapLi.addClass("active").siblings("li").removeClass("active");
			selectEnd();
		});

		// 选择完毕
		var selectEnd = function() {
			if ($.isFunction(settings.selectEnd)) {
				settings.selectEnd(getJson());
			}
			$addressPopup.modal("close");
		}

